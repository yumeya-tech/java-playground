public class Loop {
    public static void main(String[] args) {
        // for文
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }

        // while文
        int j = 5;
        while (j > 0) {
            System.out.println("j = " + j);
            j--;
        }
    }
}
